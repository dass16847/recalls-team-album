// Helper function to get card image by name
export const getCardImage = (cardName) => {
  console.log('=== getCardImage DEBUG ===');
  console.log('Input cardName:', cardName);

  const upperCaseName = cardName.toUpperCase();
  const image = imageMap[upperCaseName];

  if (!image) {
    console.warn(`No image found for: ${cardName}`);
    return null;
  }

  console.log('Image found successfully for:', cardName);
  return image;
};

